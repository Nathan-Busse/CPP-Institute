#include <stdio.h>

int main()
{
    int i; // Syntax to declare an integer variable
    float x; // Syntax to declare a float variable

    i = 10 / 4; // Integer variable value
    x = 10.0 / 4.0; // Float variable value

    printf("Output of integer variable value is: %d \n", i);
    printf("Output of float variable value is: %f\n", x);
    return 0;
}
